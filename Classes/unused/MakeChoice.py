class MakeChoice:
    def __init__(self, team_name, options, choice):
        self.team_name = team_name
        self.options = options
        self.choice = choice

        def getOptions(self):
            return self.options

        def setOptions(self, options):
            self.options = options

        def setTeamChoice(self, teamChoice):
            self.teamChoice = teamChoice

        def getTeamChoice(self):
            return self.teamChoice
